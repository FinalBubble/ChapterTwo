package org.finalbubble._运算符;


/*
*       字符的加操作
*           1、先把字符转成对应的ASCII码
*           2、参与运算
* */
public class Demo4 {
    public static void main(String[] args) {
        System.out.println('a'+1); //'a'= 97
        System.out.println('A'+1); //'A'= 65
        System.out.println('0'+1); //'0'= 48
    }
}
