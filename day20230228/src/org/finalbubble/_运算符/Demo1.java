package org.finalbubble._运算符;

/*
*
*       算术运算符：
*           +： 数学的加
*           -： 数学的减
*           *： 数学的乘
*           /： 数学的除
*           %： 数学的取余
*
*
* */
public class Demo1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
